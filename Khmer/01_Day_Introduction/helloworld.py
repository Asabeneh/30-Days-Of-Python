# ការណែនាំ
# ថ្ងៃទី 1 - 30DaysOfPython Challenge

print(2 + 3)   # addition បូក(+)
print(3 - 1)   # substraction ដក(-)
print(2 * 3)   # multiplication គុណ(*)
print(3 / 2)   # division ចែក(/)
print(3 ** 2)  # exponential អ៊ិចស្ប៉ូណង់ស្យែល(**)
print(3 % 2)   # modulus ម៉ូឌុល(%)
print(3 // 2)  # floor divsion operator ចែកយកចំនួនគត់(//)

# មើលប្រភេទទិន្នន័យ

print(type(10))                  # Int = ចំនួនគត់
print(type(3.14))                # Float = ទសភាគ
print(type(1 + 3j))              # Complexx ចំនួនកុំផ្លិច
print(type('Asabeneh'))          # String = ទិន្ន័យខ្សែអក្សរ
print(type([1, 2, 3]))           # List = បញ្ជី ដែលតាមលំដាប់, អាចផ្លាស់ប្ដូរ, និងអាចដូចគ្នាបាន 
print(type({'name':'Asabeneh'})) # Dictionary = បញ្ជីដែលមានឈ្មោះតម្លៃនិងតម្លៃរបស់វា ដែលតាមលំដាប់, អាចផ្លាស់ប្ដូរ, បន្តែមិនអាចដូចគ្នាបាន
print(type({9.8, 3.14, 2.7}))    # Set = បញ្ចី ដែលមិនមានតាមលំដាប់ និង មិនអាចដូចគ្នា
print(type((9.8, 3.14, 2.7)))    # Tuple = បញ្ចី ដែលតាមលំដាប់, ប៉ុន្តែមិនអាចប្តូរបាន
